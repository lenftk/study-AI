#복합 연산자
a = 0
a = a + 1 # 수열처럼 생각하자 a항을 기존 a항에서 1을 더한것으로 정의한다는 의미
# a += 1 # 위와 동일한 의미
a = a - 1 # a항을 기존 a항에서 1을 뺀것으로 정의한다는 의미
# a -= 1 # 위와 동일한 의미
a = a * 2 # a항을 기존 a항에서 2를 곱한것으로 정의한다는 의미
# a *= 2 # 위와 동일한 의미
a = a / 2 # a항을 기존 a항에서 2를 나눈것으로 정의한다는 의미
# a /= 2 # 위와 동일한 의미
a = a // 2 # a항을 기존 a항에서 2를 나눈 몫으로 정의한다는 의미
# a //= 2 # 위와 동일한 의미
a = a % 2 # a항을 기존 a항에서 2를 나눈 나머지로 정의한다는 의미
# a %= 2 # 위와 동일한 의미
a = a ** 2 # a항을 기존 a항에서 2를 제곱한것으로 정의한다는 의미
# a **= 2 # 위와 동일한 의미

